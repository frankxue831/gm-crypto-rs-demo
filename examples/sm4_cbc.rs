use gmcrypto_core::sm4::mode_cbc;

const DEMO_SM4_KEY: [u8; 16] = [
    0x01, 0x23, 0x45, 0x67, 0x89, 0xab, 0xcd, 0xef, 0xfe, 0xdc, 0xba, 0x98, 0x76, 0x54, 0x32, 0x10,
];
const DEMO_SM4_IV: [u8; 16] = [
    0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
];

fn main() {
    let ciphertext = mode_cbc::encrypt(&DEMO_SM4_KEY, &DEMO_SM4_IV, b"hello sdk");
    let plaintext = mode_cbc::decrypt(&DEMO_SM4_KEY, &DEMO_SM4_IV, &ciphertext).expect("decrypt");

    println!("{}", String::from_utf8_lossy(&plaintext));
}
